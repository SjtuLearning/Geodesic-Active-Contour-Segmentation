function s=s(p1,p2,p3)
% 求三角形的面积
% p=1/2(a+b+c) 
% s=sqrt(p*(p-a)(p-b)(p-c))
a = sqrt((p1(1)-p2(1))^2+(p1(2)-p2(2))^2);
b = sqrt((p2(1)-p3(1))^2+(p2(2)-p3(2))^2);
c = sqrt((p1(1)-p3(1))^2+(p1(2)-p3(2))^2);
p = 1/2*(a+b+c);    %三角形周长
s = sqrt(p*(p-a)*(p-b)*(p-c));
